// ─── OPEN A CATEGORY ──────────────────────────────────────────
function openCategory(key) {
  const category = adhkarData[key];
  if (!category) return;

  currentCategory = key;
  counters = category.dhikr.map(function() { return 0; });

  document.getElementById('category-title').textContent = category.title;
  buildDhikrList(category.dhikr);
  updateProgress();
  showScreen('screen-list');
}

// ─── BUILD DHIKR CARDS ────────────────────────────────────────
function buildDhikrList(dhikrArray) {
  const container = document.getElementById('dhikr-list');
  container.innerHTML = '';

  dhikrArray.forEach(function(dhikr, index) {
    let dotsHTML = '';
    if (dhikr.repeat <= 20) {
      let dots = '';
      for (let i = 0; i < dhikr.repeat; i++) {
        dots += '<div class="dot" id="dot-' + index + '-' + i + '"></div>';
      }
      dotsHTML = '<div class="repeat-dots">' + dots + '</div>';
    }

    container.innerHTML += `
      <div class="dhikr-card" id="card-${index}">
        <div class="arabic-text">${dhikr.arabic.replace(/\n/g, '<br>')}</div>
        ${dotsHTML}
        <div class="dhikr-divider"></div>
        <div class="meaning-text">${dhikr.meaning}</div>
        <div class="source-text">${dhikr.source}</div>
        <div class="dhikr-footer">
          <div class="virtue-badge">${dhikr.virtue}</div>
          <div style="display:flex; gap:8px; align-items:center;">
            <button class="reset-card-btn" onclick="resetCard(${index})" title="Reset">🔄</button>
            <button class="counter-btn" id="btn-${index}" onclick="tap(${index})">
              <span class="counter-num" id="num-${index}">0</span>
              <span class="counter-total">/ ${dhikr.repeat}</span>
            </button>
          </div>
        </div>
      </div>
    `;
  });
}

// ─── TAP COUNTER ──────────────────────────────────────────────
function tap(index) {
  const target = adhkarData[currentCategory].dhikr[index].repeat;
  if (counters[index] >= target) return;

  counters[index]++;
  const current = counters[index];

  document.getElementById('num-' + index).textContent = current;

  const dot = document.getElementById('dot-' + index + '-' + (current - 1));
  if (dot) dot.classList.add('filled');

  const btn = document.getElementById('btn-' + index);
  btn.style.transform = 'scale(0.93)';
  setTimeout(function() { btn.style.transform = 'scale(1)'; }, 100);

  if (current >= target) markDone(index);
  updateProgress();
}

// ─── MARK DONE ────────────────────────────────────────────────
function markDone(index) {
  document.getElementById('card-' + index).classList.add('done');
  const btn = document.getElementById('btn-' + index);
  btn.classList.add('completed');
  btn.innerHTML = '<span class="check-icon">✅</span>';
  checkAllComplete();
}

// ─── RESET A SINGLE CARD ──────────────────────────────────────
function resetCard(index) {
  const target = adhkarData[currentCategory].dhikr[index].repeat;
  counters[index] = 0;

  const btn = document.getElementById('btn-' + index);
  btn.classList.remove('completed');
  btn.innerHTML = `
    <span class="counter-num" id="num-${index}">0</span>
    <span class="counter-total">/ ${target}</span>
  `;
  btn.onclick = function() { tap(index); };

  document.getElementById('card-' + index).classList.remove('done');

  for (let i = 0; i < target; i++) {
    const dot = document.getElementById('dot-' + index + '-' + i);
    if (dot) dot.classList.remove('filled');
  }

  updateProgress();
}

// ─── CHECK ALL COMPLETE ───────────────────────────────────────
function checkAllComplete() {
  const allDone = counters.every(function(count, i) {
    return count >= adhkarData[currentCategory].dhikr[i].repeat;
  });
  if (allDone) {
    setTimeout(function() { showScreen('screen-complete'); }, 800);
  }
}

// ─── PROGRESS BAR ─────────────────────────────────────────────
function updateProgress() {
  const dhikr = adhkarData[currentCategory].dhikr;
  const total = dhikr.length;
  let done = 0;
  counters.forEach(function(count, i) {
    if (count >= dhikr[i].repeat) done++;
  });
  const pct = (done / total) * 100;
  document.getElementById('progress-bar').style.width = pct + '%';
  document.getElementById('progress-text').textContent = done + ' / ' + total;
}

