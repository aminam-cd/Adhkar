function openSurah(surahNum) {
  document.getElementById('quran-surah-list').style.display = 'none';
  document.getElementById('quran-ayah-view').style.display = 'block';
  document.getElementById('surah-title-bar').textContent = surahNames[surahNum - 1];

  const container = document.getElementById('quran-ayahs');
  container.innerHTML = '<div style="text-align:center; padding:40px; color:var(--text-dim);">جاري التحميل...</div>';

  fetch('https://api.alquran.cloud/v1/surah/' + surahNum)
    .then(function(res) { return res.json(); })
    .then(function(data) {
      container.innerHTML = '';
      const ayahs = data.data.ayahs;
      ayahs.forEach(function(ayah) {
        container.innerHTML += `
          <div class="dhikr-card">
            <div style="display:flex; justify-content:space-between; align-items:center; margin-bottom:12px;">
              <div style="
                background:var(--accent-glow);
                border:1px solid var(--accent);
                color:var(--accent);
                border-radius:50%;
                width:30px; height:30px;
                display:flex; align-items:center; justify-content:center;
                font-size:0.75rem; font-weight:700;
              ">${ayah.numberInSurah}</div>
            </div>
            <div class="arabic-text" style="font-size:1.5rem;">${ayah.text}</div>
          </div>
        `;
      });
    })
    .catch(function() {
      container.innerHTML = '<div style="text-align:center; padding:40px; color:red;">فشل التحميل. تحقق من الإنترنت.</div>';
    });
}

